/**
 * Generate placeholder pixel data for a team's first letter.
 * Returns 1-indexed pixel IDs for a 32x32 grid.
 * The letter is centered around position (14, 13) in the grid.
 */
export function getPlaceholderPixels(letter: string): number[] {
  const upper = letter.toUpperCase();
  const bitmap = LETTER_BITMAPS[upper];
  if (!bitmap) return [];

  // Center the 5x7 letter in the 32x32 grid
  // Top-left corner of the letter block
  const startRow = 12; // vertically centered: (32 - 7) / 2 ~ 12
  const startCol = 13; // horizontally centered: (32 - 5) / 2 ~ 13

  const pixels: number[] = [];
  for (const [r, c] of bitmap) {
    const gridRow = startRow + r;
    const gridCol = startCol + c;
    if (gridRow >= 0 && gridRow < 32 && gridCol >= 0 && gridCol < 32) {
      // pixelId is 1-indexed: row * 32 + col + 1
      pixels.push(gridRow * 32 + gridCol + 1);
    }
  }

  return pixels;
}
