interface MerchIconProps {
  kind: MerchKind;
  size?: number;
  className?: string;
  /** Accessible label. If omitted, the icon is purely decorative. */
  label?: string;
}

export function MerchIcon({ kind, size = 64, className, label }: MerchIconProps) {
  const rects = ICONS[kind] ?? ICONS.tee;
  return (
    <svg
      viewBox={`0 0 ${G} ${G}`}
      width={size}
      height={size}
      shapeRendering="crispEdges"
      fill="currentColor"
      className={className}
      style={{ display: 'block' }}
      role={label ? 'img' : undefined}
      aria-label={label}
      aria-hidden={label ? undefined : true}
    >
      {label && <title>{label}</title>}
      {rects.map(([x, y, w, h, bg], i) => (
        <rect
          key={i}
          x={x}
          y={y}
          width={w}
          height={h}
          style={bg ? { fill: 'var(--bg)' } : undefined}
        />
      ))}
    </svg>
  );
}

// Map a product title/handle to the closest brand icon.
export function merchKindFor(handleOrTitle: string): MerchKind {
  const s = handleOrTitle.toLowerCase();
  if (s.includes('jersey') || s.includes('kit')) return 'kit';
  if (s.includes('hoodie')) return 'hoodie';
  if (s.includes('short')) return 'shorts';
  if (s.includes('sock')) return 'sock';
  if (s.includes('beanie')) return 'beanie';
  if (s.includes('scarf')) return 'scarf';
  if (s.includes('cap') || s.includes('hat')) return 'cap';
  if (s.includes('mug')) return 'mug';
  if (s.includes('tote') || s.includes('bag')) return 'tote';
  if (s.includes('tee') || s.includes('shirt') || s.includes('tshirt') || s.includes('t-shirt')) return 'tee';
  if (s.includes('sticker') || s.includes('ball')) return 'ball';
  if (s.includes('poster') || s.includes('print')) return 'field';
  return 'tee';
}
