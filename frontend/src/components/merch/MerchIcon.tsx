interface MerchIconProps {
  kind: MerchKind;
  size?: number;
  className?: string;
}

export function MerchIcon({ kind, size = 64, className }: MerchIconProps) {
  const grid = ICONS[kind];
  const rects: React.ReactElement[] = [];
  for (let y = 0; y < G; y++) {
    const row = grid[y] ?? '';
    for (let x = 0; x < G; x++) {
      if (row[x] === '#') {
        rects.push(<rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} />);
      }
    }
  }
  return (
    <svg
      viewBox={`0 0 ${G} ${G}`}
      width={size}
      height={size}
      shapeRendering="crispEdges"
      fill="currentColor"
      className={className}
      style={{ display: 'block' }}
      aria-hidden="true"
    >
      {rects}
    </svg>
  );
}

export function merchKindFor(handleOrTitle: string): MerchKind {
  const s = handleOrTitle.toLowerCase();
  if (s.includes('mug')) return 'mug';
  if (s.includes('jersey')) return 'jersey';
  if (s.includes('tee') || s.includes('shirt') || s.includes('tshirt') || s.includes('t-shirt')) return 'tee';
  if (s.includes('scarf')) return 'scarf';
  if (s.includes('cap') || s.includes('hat')) return 'cap';
  if (s.includes('sticker')) return 'stickers';
  if (s.includes('poster') || s.includes('print')) return 'poster';
  return 'tee';
}
